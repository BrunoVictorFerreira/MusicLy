                        <b style='color: darkorange'>O que são graus musicais?</b>
                        <br /><b>Grau musical</b> é uma nomenclatura criada para ajudar o músico na localização dos intervalos. Provavelmente você já tenha ouvido falar em “primeiro grau”, “segundo grau”, etc. E talvez isso tenha soado estranho num primeiro momento. Porém, como vamos ver, essa terminologia é simples e pode ser muito útil.

                        <br /><br />Se numerássemos a escala de Dó maior da seguinte forma: Dó (1º grau), Ré (2º grau), Mi (3º grau), Fá (4º grau), Sol (5º grau), Lá (6º grau), Si (7º grau), poderíamos dizer para um amigo, por exemplo: “toque o 5º grau da escala de Dó maior”, e ele saberia que você está se referindo à nota Sol.

                        <br /><br />Por isso, acaba sendo muito útil falar das notas de uma música em termos de <b>graus musicais</b>. A lógica é a mesma que foi apresentada acima, aplicada a cada nota de interesse. Por exemplo, podemos construir os graus partindo da nota Ré:
                        <br />Ré (1º grau), Mi (2º grau), Fá (3º grau), Sol (4º grau), Lá (5º grau), Si (6º grau), Dó (7º grau).

                        <br /><br />Então, se alguém pedisse, digamos, o 3º grau de Ré, você saberia que se trata da nota Fá. Observe que estamos trabalhando dentro da escala de dó maior nesses exemplos todos. Isso precisa ser especificado (em qual escala estamos trabalhando).

                        <br /><br />De uma maneira prática, para saber a nota que se refere a algum grau basta contar nos dedos as notas partindo da nota que foi definida como 1º grau. Abaixo seguem alguns exemplos, ainda dentro da escala de dó maior (tome como exercício):

                        <br /><br />– Segundo grau de Mi: Fá
                        <br />– Quarto grau de Sol: Dó
                        <br />– Sétimo grau de Si: Lá

                        <br /><br />Obs: O primeiro grau é também chamado de “<b>tônica</b>”.

                        <br /><br /><b style='color: darkorange'>Mais detalhes sobre graus musicais</b>
                        <br />Esses exemplos foram utilizados apenas para fins didáticos. Na prática, você verá que os graus são muito utilizados dentro do contexto de campos harmônicos. Você aprenderá como se situar numa música utilizando graus no artigo “campo harmônico”.

                        <br /><br />Antes disso, aprenderemos (nos tópicos “diminuta, aumentada e justa” e “graus musicais – conceito complementar”) outros detalhes importantes sobre graus.

